class SensitivityEngine:
    """Compute ∂NPV/∂feature for each substation via numerical differentiation.

    Provides Layer 2 with per-substation sensitivity vectors:
    "If feature X increases by 1 unit, NPV changes by Y €M."
    """

    def __init__(self, trainer: NeuralNetworkTrainer, epsilon: float = 0.01):
        self.trainer = trainer
        self.epsilon = epsilon
        self.model = trainer.models.get('multi_target', trainer.models.get('npv_regressor'))
        self.scaler = trainer.scaler

    def _predict_p50(self, X_scaled):
        pred = self.model.predict(X_scaled)
        if pred.ndim == 2:
            return pred[:, 1]
        return pred

    def compute_jacobian(self, n_sample: int = 500, verbose: bool = True) -> dict:
        """Compute numerical Jacobian for a sample of substations.

        Returns:
            dict with 'jacobian' (n_sample × n_features), 'global_sensitivity',
            and 'per_substation' sensitivities.
        """
        if verbose:
            print(f"\n[B] Jacobian Sensitivity Engine (ε={self.epsilon}, n={n_sample})")
            print(f"{'-'*70}")

        rng = np.random.RandomState(42)
        n_total = len(self.trainer.X)
        sample_idx = rng.choice(n_total, size=min(n_sample, n_total), replace=False)

        X_sample = self.trainer.X[sample_idx]
        X_scaled = self.scaler.transform(X_sample)
        base_pred = self._predict_p50(X_scaled)

        n_features = X_sample.shape[1]
        jacobian = np.zeros((len(sample_idx), n_features))

        for j in range(n_features):
            X_pert = X_scaled.copy()
            # Perturb by epsilon in scaled space
            X_pert[:, j] += self.epsilon
            pert_pred = self._predict_p50(X_pert)
            jacobian[:, j] = (pert_pred - base_pred) / self.epsilon

        # Global sensitivity: mean |∂NPV/∂feature|
        global_sens = np.abs(jacobian).mean(axis=0)
        fi_sorted = np.argsort(global_sens)[::-1]

        top_sensitivities = [
            {
                'feature': ALL_FEATURES[i],
                'mean_abs_sensitivity': round(float(global_sens[i]), 4),
                'direction': 'positive' if jacobian[:, i].mean() > 0 else 'negative',
            }
            for i in fi_sorted[:15]
        ]

        # Per-substation top-3 sensitivities
        per_sub = {}
        for k, idx in enumerate(sample_idx):
            sub_id = self.trainer.df.iloc[idx]['substation_id']
            jac = jacobian[k]
            top3_idx = np.argsort(np.abs(jac))[::-1][:3]
            per_sub[sub_id] = [
                {'feature': ALL_FEATURES[j], 'dNPV_per_unit': round(float(jac[j]), 4)}
                for j in top3_idx
            ]

        metrics = {
            'n_substations': len(sample_idx),
            'n_features': n_features,
            'epsilon': self.epsilon,
            'top_sensitivities': top_sensitivities,
            'per_substation_count': len(per_sub),
        }

        if verbose:
            print(f"  Computed {len(sample_idx)} × {n_features} Jacobian matrix")
            print(f"  Top global sensitivities:")
            for s in top_sensitivities[:10]:
                print(f"    {s['feature']:25s}  |∂NPV/∂f|={s['mean_abs_sensitivity']:.4f}  dir={s['direction']}")

        return {'jacobian': jacobian, 'sample_idx': sample_idx, 'metrics': metrics,
                'global': top_sensitivities, 'per_substation': per_sub}



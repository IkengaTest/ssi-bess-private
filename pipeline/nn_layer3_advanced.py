class DriftMonitor:
    """Detect when model retraining is needed.

    Uses Population Stability Index (PSI) to track feature and prediction
    distribution shifts between training and new data.
    """

    @staticmethod
    def _psi(expected: np.ndarray, actual: np.ndarray, n_bins: int = 10) -> float:
        """Compute Population Stability Index."""
        # Create bins from expected distribution
        breakpoints = np.quantile(expected, np.linspace(0, 1, n_bins + 1))
        breakpoints[0] = -np.inf
        breakpoints[-1] = np.inf

        expected_counts = np.histogram(expected, bins=breakpoints)[0]
        actual_counts = np.histogram(actual, bins=breakpoints)[0]

        # Add smoothing to avoid log(0)
        expected_pct = (expected_counts + 1) / (len(expected) + n_bins)
        actual_pct = (actual_counts + 1) / (len(actual) + n_bins)

        psi = np.sum((actual_pct - expected_pct) * np.log(actual_pct / expected_pct))
        return float(psi)

    def __init__(self, trainer: NeuralNetworkTrainer):
        self.trainer = trainer

    def compute_baseline(self, verbose: bool = True) -> dict:
        """Compute baseline distributions from training data for future monitoring."""
        if verbose:
            print(f"\n[D] Model Monitoring — Baseline Computation")
            print(f"{'-'*70}")

        X_train = self.trainer.X[self.trainer.train_idx]
        X_test = self.trainer.X[self.trainer.test_idx]

        # Feature distribution baselines
        feature_stats = {}
        for j, feat in enumerate(ALL_FEATURES):
            train_vals = X_train[:, j]
            feature_stats[feat] = {
                'mean': round(float(np.mean(train_vals)), 6),
                'std': round(float(np.std(train_vals)), 6),
                'p5': round(float(np.percentile(train_vals, 5)), 6),
                'p95': round(float(np.percentile(train_vals, 95)), 6),
            }

        # Prediction distribution baseline
        model = self.trainer.models.get('multi_target', self.trainer.models.get('npv_regressor'))
        X_train_scaled = self.trainer.scaler.transform(X_train)
        pred_train = model.predict(X_train_scaled)
        if pred_train.ndim == 2:
            p50_train = pred_train[:, 1]
        else:
            p50_train = pred_train

        pred_stats = {
            'mean': round(float(np.mean(p50_train)), 4),
            'std': round(float(np.std(p50_train)), 4),
            'p5': round(float(np.percentile(p50_train, 5)), 4),
            'p95': round(float(np.percentile(p50_train, 95)), 4),
        }

        # Compute train vs test PSI as reference
        feature_psi = {}
        for j, feat in enumerate(ALL_FEATURES):
            psi = self._psi(X_train[:, j], X_test[:, j])
            feature_psi[feat] = round(psi, 6)

        # Sort by PSI (highest drift features)
        psi_sorted = sorted(feature_psi.items(), key=lambda x: x[1], reverse=True)

        X_test_scaled = self.trainer.scaler.transform(X_test)
        pred_test = model.predict(X_test_scaled)
        if pred_test.ndim == 2:
            p50_test = pred_test[:, 1]
        else:
            p50_test = pred_test

        pred_psi = self._psi(p50_train, p50_test)

        n_drifted = sum(1 for _, psi in psi_sorted if psi > 0.10)

        metrics = {
            'n_features': len(ALL_FEATURES),
            'feature_psi': dict(psi_sorted[:10]),
            'prediction_psi': round(pred_psi, 6),
            'n_features_drifted': n_drifted,
            'drift_threshold': 0.10,
            'retrain_recommended': pred_psi > 0.20 or n_drifted > 5,
            'feature_baselines': feature_stats,
            'prediction_baseline': pred_stats,
        }

        if verbose:
            print(f"  Train/test PSI (prediction): {pred_psi:.6f}")
            print(f"  Features with PSI > 0.10:    {n_drifted}/{len(ALL_FEATURES)}")
            print(f"  Top drifted features:")
            for feat, psi in psi_sorted[:5]:
                status = '⚠' if psi > 0.10 else '✓'
                print(f"    {status} {feat:25s}  PSI={psi:.6f}")
            print(f"  Retrain recommended: {'YES' if metrics['retrain_recommended'] else 'NO'}")

        return metrics



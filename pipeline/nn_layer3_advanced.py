class EnsembleUncertainty:
    """Decompose prediction uncertainty into epistemic and aleatoric.

    Uses bootstrap ensemble: train N models on bootstrap samples.
    - Epistemic uncertainty = variance across models (fixable with more data)
    - Aleatoric uncertainty = mean of within-model variance (irreducible)

    Investment committees need this: "we're uncertain because we lack data"
    vs "we're uncertain because the market is genuinely volatile."
    """

    def __init__(self, trainer: NeuralNetworkTrainer, n_models: int = 10):
        self.trainer = trainer
        self.n_models = n_models

    def train_ensemble(self, verbose: bool = True) -> dict:
        """Train bootstrap ensemble and decompose uncertainty."""
        if verbose:
            print(f"\n[E] Ensemble Uncertainty Decomposition ({self.n_models} bootstrap models)")
            print(f"{'-'*70}")

        rng = np.random.RandomState(42)
        X_train = self.trainer.X_train
        Y_train = self.trainer.df[MULTI_TARGETS].fillna(0).values[self.trainer.train_idx]

        # Train N bootstrap models
        models = []
        for i in range(self.n_models):
            # Bootstrap sample (with replacement)
            n = len(X_train)
            boot_idx = rng.choice(n, size=n, replace=True)
            X_boot = X_train[boot_idx]
            Y_boot = Y_train[boot_idx]

            base = MLPRegressor(
                hidden_layer_sizes=(256, 128),  # Slightly smaller for speed
                activation='relu', solver='adam', alpha=1e-3,
                learning_rate='adaptive', max_iter=300,
                random_state=42 + i,
                early_stopping=True, validation_fraction=0.1,
                n_iter_no_change=15,
            )
            model = MultiOutputRegressor(base, n_jobs=-1)
            model.fit(X_boot, Y_boot)
            models.append(model)
            if verbose:
                print(f"  Model {i+1}/{self.n_models} trained", end='\r')

        if verbose:
            print(f"  All {self.n_models} models trained             ")

        # Predict with all models on full dataset
        X_all_scaled = self.trainer.scaler.transform(self.trainer.X)
        all_predictions = np.zeros((self.n_models, len(self.trainer.X)))

        for i, model in enumerate(models):
            pred = model.predict(X_all_scaled)
            if pred.ndim == 2:
                all_predictions[i] = pred[:, 1]  # P50
            else:
                all_predictions[i] = pred

        # Decompose uncertainty
        # Epistemic: variance across models (what the model doesn't know)
        epistemic = np.var(all_predictions, axis=0)

        # Total variance from MC: compare ensemble mean to actual MC targets
        mc_p50 = self.trainer.df['npv_b'].values
        ensemble_mean = np.mean(all_predictions, axis=0)
        total_residual_var = (ensemble_mean - mc_p50) ** 2

        # Aleatoric: MC-derived spread (P95 - P5) as proxy for irreducible variance
        mc_p5 = self.trainer.df['npv_P5'].values
        mc_p95 = self.trainer.df['npv_P95'].values
        aleatoric = ((mc_p95 - mc_p5) / 3.29) ** 2  # Approximate variance from 90% CI

        # Fractions
        total = epistemic + aleatoric + 1e-10
        epistemic_frac = epistemic / total
        aleatoric_frac = aleatoric / total

        # Per-substation uncertainty classification
        per_sub = {}
        for i in range(len(self.trainer.df)):
            sub_id = self.trainer.df.iloc[i]['substation_id']
            per_sub[sub_id] = {
                'epistemic_M2': round(float(epistemic[i]), 6),
                'aleatoric_M2': round(float(aleatoric[i]), 6),
                'epistemic_pct': round(float(epistemic_frac[i] * 100), 1),
                'uncertainty_type': 'data_limited' if epistemic_frac[i] > 0.5 else 'market_volatile',
                'ensemble_std_M': round(float(np.sqrt(epistemic[i])), 4),
                'ensemble_mean_M': round(float(ensemble_mean[i]), 4),
            }

        # Global statistics
        metrics = {
            'n_models': self.n_models,
            'epistemic_mean_M2': round(float(np.mean(epistemic)), 6),
            'aleatoric_mean_M2': round(float(np.mean(aleatoric)), 6),
            'epistemic_pct_fleet': round(float(np.mean(epistemic_frac) * 100), 1),
            'aleatoric_pct_fleet': round(float(np.mean(aleatoric_frac) * 100), 1),
            'n_data_limited': int(np.sum(epistemic_frac > 0.5)),
            'n_market_volatile': int(np.sum(epistemic_frac <= 0.5)),
            'ensemble_std_mean_M': round(float(np.mean(np.sqrt(epistemic))), 4),
            'ensemble_agreement': round(float(np.mean(np.std(all_predictions, axis=0) < 0.5)), 4),
            'per_substation_count': len(per_sub),
        }

        if verbose:
            print(f"  Epistemic (model uncertainty): {metrics['epistemic_pct_fleet']:.1f}% of total")
            print(f"  Aleatoric (market volatility): {metrics['aleatoric_pct_fleet']:.1f}% of total")
            print(f"  Data-limited substations:      {metrics['n_data_limited']}")
            print(f"  Market-volatile substations:   {metrics['n_market_volatile']}")
            print(f"  Ensemble std (mean):           €{metrics['ensemble_std_mean_M']:.4f}M")
            print(f"  Model agreement (std<€0.5M):   {metrics['ensemble_agreement']*100:.1f}%")

        return {'models': models, 'predictions': all_predictions,
                'metrics': metrics, 'per_substation': per_sub}


# ═══════════════════════════════════════════════════════════════
# Orchestrator: Run All 5 Advanced Modules
# ═══════════════════════════════════════════════════════════════

def run_all_advanced(trainer: NeuralNetworkTrainer, substations: list,
                     verbose: bool = True) -> dict:
    """Run all 5 advanced Layer 3 modules and return combined metrics."""
    t0 = time.time()

    if verbose:
        print(f"\n{'='*70}")
        print(f"  SSI-ENN BESS — Advanced Layer 3 Modules (v3.5)")
        print(f"{'='*70}")

    results = {}

    # A. Config A Parallel
    dual = DualConfigTrainer(trainer)
    model_a, metrics_a = dual.train(substations, verbose=verbose)
    results['config_a'] = metrics_a
    results['config_a_model'] = model_a

    # B. Sensitivity Engine
    sens = SensitivityEngine(trainer)
    sens_results = sens.compute_jacobian(verbose=verbose)
    results['sensitivity'] = sens_results['metrics']
    results['sensitivity_global'] = sens_results['global']
    results['sensitivity_per_sub'] = sens_results['per_substation']

    # C. Stress Testing
    stress = ScenarioStressTester(trainer)
    results['stress'] = stress.run_all_scenarios(verbose=verbose)

    # D. Drift Monitoring
    drift = DriftMonitor(trainer)
    results['drift'] = drift.compute_baseline(verbose=verbose)

    # E. Ensemble Uncertainty
    ensemble = EnsembleUncertainty(trainer, n_models=10)
    ens_results = ensemble.train_ensemble(verbose=verbose)
    results['ensemble'] = ens_results['metrics']
    results['ensemble_per_sub'] = ens_results['per_substation']

    duration = time.time() - t0

    if verbose:
        print(f"\n{'='*70}")
        print(f"  ADVANCED MODULES COMPLETE — {duration:.1f}s")
        print(f"{'='*70}")
        print(f"  [A] Config A:    5-target regressor trained")
        print(f"  [B] Sensitivity: {results['sensitivity']['n_substations']} substations × {results['sensitivity']['n_features']} features")
        print(f"  [C] Stress:      {results['stress']['pass_rate']:.0f}% scenarios pass")
        print(f"  [D] Drift:       retrain={'YES' if results['drift']['retrain_recommended'] else 'NO'}")
        print(f"  [E] Ensemble:    {results['ensemble']['epistemic_pct_fleet']:.1f}% epistemic / {results['ensemble']['aleatoric_pct_fleet']:.1f}% aleatoric")

    results['_meta'] = {
        'version': 3.5,
        'duration_s': round(duration, 2),
        'modules': ['config_a', 'sensitivity', 'stress', 'drift', 'ensemble'],
    }

    return results

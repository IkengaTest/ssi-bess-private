class DualConfigTrainer:
    """Train a separate multi-target regressor for Config A.

    Uses mc_results_A from data.json (generated by run_fleet_mc with config='A').
    Produces: NPV_A P5/P50/P95, IRR_A, Sharpe_A — parallel to Config B targets.
    """

    TARGETS_A = ['npv_A_P5', 'npv_A_P50', 'npv_A_P95', 'irr_A', 'sharpe_A']
    LABELS_A = ['NPV_A P5', 'NPV_A P50', 'NPV_A P95', 'IRR_A', 'Sharpe_A']

    def __init__(self, trainer: NeuralNetworkTrainer):
        self.trainer = trainer
        self.df = trainer.df
        self.X_train = trainer.X_train
        self.X_test = trainer.X_test
        self.train_idx = trainer.train_idx
        self.test_idx = trainer.test_idx
        self.scaler = trainer.scaler
        self.X = trainer.X

    def _extract_config_a_targets(self, substations: list) -> pd.DataFrame:
        """Extract Config A MC targets from substations."""
        rows = []
        for s in substations:
            mc_a = s.get('mc_results_A', {})
            rows.append({
                'npv_A_P5': mc_a.get('npv', {}).get('npv_P5', 0),
                'npv_A_P50': mc_a.get('npv', {}).get('npv_P50', 0),
                'npv_A_P95': mc_a.get('npv', {}).get('npv_P95', 0),
                'irr_A': mc_a.get('irr', {}).get('irr_median', 0),
                'sharpe_A': mc_a.get('risk', {}).get('sharpe_ratio', 0),
            })
        return pd.DataFrame(rows)

    def train(self, substations: list, verbose: bool = True) -> dict:
        """Train Config A multi-target regressor."""
        if verbose:
            print(f"\n[A] Config A Multi-Target Regressor — MLP(512,256,128)")
            print(f"{'-'*70}")

        df_a = self._extract_config_a_targets(substations)

        Y = df_a[self.TARGETS_A].fillna(0).values.astype(np.float64)
        Y_train, Y_test = Y[self.train_idx], Y[self.test_idx]

        base_model = MLPRegressor(
            hidden_layer_sizes=(512, 256, 128),
            activation='relu', solver='adam', alpha=1e-3,
            learning_rate='adaptive', max_iter=500,
            random_state=42,
            early_stopping=True, validation_fraction=0.1,
            n_iter_no_change=20,
        )
        model = MultiOutputRegressor(base_model, n_jobs=-1)
        model.fit(self.X_train, Y_train)

        pred_test = model.predict(self.X_test)
        pred_train = model.predict(self.X_train)

        per_target = {}
        for j, (name, label) in enumerate(zip(self.TARGETS_A, self.LABELS_A)):
            yt, yp = Y_test[:, j], pred_test[:, j]
            yt_tr, yp_tr = Y_train[:, j], pred_train[:, j]
            sp, _ = spearmanr(yt, yp)
            per_target[name] = {
                'label': label,
                'mae_test': round(float(mean_absolute_error(yt, yp)), 4),
                'r2_test': round(float(r2_score(yt, yp)), 4),
                'spearman': round(float(sp), 4),
            }

        # Delta analysis: A vs B
        X_all = self.scaler.transform(self.X)
        pred_A_all = model.predict(X_all)
        pred_B_all = self.trainer.models.get('multi_target', self.trainer.models.get('npv_regressor')).predict(X_all)

        if pred_B_all.ndim == 2:
            delta_npv = pred_B_all[:, 1] - pred_A_all[:, 1]  # B_P50 - A_P50
        else:
            delta_npv = pred_B_all - pred_A_all[:, 1]

        metrics = {
            'per_target': per_target,
            'delta_npv_mean_M': round(float(delta_npv.mean()), 4),
            'delta_npv_std_M': round(float(delta_npv.std()), 4),
            'pct_B_dominates': round(float(np.mean(delta_npv > 0) * 100), 1),
        }

        if verbose:
            for name, tm in per_target.items():
                print(f"  {tm['label']:12s}  MAE={tm['mae_test']:.4f}  R²={tm['r2_test']:.4f}  ρ={tm['spearman']:.4f}")
            print(f"  ΔN PV (B−A) mean: €{metrics['delta_npv_mean_M']:.2f}M ± €{metrics['delta_npv_std_M']:.2f}M")
            print(f"  Config B dominates: {metrics['pct_B_dominates']:.1f}% of substations")

        return model, metrics


